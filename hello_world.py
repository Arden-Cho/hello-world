import os
from time import sleep
from sys import platform

def refresh(time):
    sleep(time)
    if platform == "linux" or platform == "linux2": # Linux
        os.system("clear")
    elif platform == "darwin": # macOS
        os.system("clear")
    elif platform == "win32": # Windows
        os.system("CLS")

